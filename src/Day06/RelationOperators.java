package Day06;

public class RelationOperators {
    public static void main(String[] args) {

        boolean g = 10>9;
        System.out.println("1."+g);
        boolean f = 1000<5;
        System.out.println("2."+f);
        boolean d = 8==8;
        System.out.println("3."+d);
        boolean p = 10!=10;
        System.out.println("4."+p);
        boolean o = "AAA"=="aaa";
        System.out.println("5."+o);
        boolean i = "sss"=="sss";
        System.out.println("6."+i);
        boolean u = "ddd"!="fff";
        System.out.println("7."+u);
        boolean y = true!=false;
        System.out.println("8."+y);

        System.out.println("====================");
        char t = 'A';
        System.out.println(t);
        boolean r = 100==100.0;
        System.out.println(r);
        boolean e = 101%2==0;
        boolean w = 101%2!=0;
        System.out.println(e);
        System.out.println(w);










    }
}
