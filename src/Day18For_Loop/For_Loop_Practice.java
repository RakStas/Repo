package Day18For_Loop;

public class For_Loop_Practice {
    public static void main(String[] args) {
        System.out.println("Cybertec Batch 20");
        for(int i=1; i<=5; i+=1){
            System.out.println("Cybertec Batch 20");
        }
        System.out.println("=====================");
        for(int i=1; i<=10; i+=1){
            System.out.print(i+" ");
        }
        System.out.println("=================");

        for(int i=10; i>=1; i--) {
            System.out.print(i+" ");
        }







    }
}
