package Day20_WhileLoop;

public class WhileLoopPactice {
    public static void main(String[] args) {
        for(int i=1; i<=10; i++){

            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("===================");
        System.out.println("WhileLoop");



        int num=1;
        while (num<=10){
            System.out.print(num+" ");
            num++;

        }





    }
}
