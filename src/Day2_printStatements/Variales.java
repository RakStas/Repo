package Day2_printStatements;

public class Variales {
    public static void main(String[] args) {

        byte length = -128;
        byte width = 41;
        System.out.println(length*width);
        double temperature = 214.5;
        double cooling = 25.95;

        short num2 = 150;
        System.out.println(temperature-cooling+num2);
        int num3 = 456789;
        int num4 = 999;
        System.out.println(num3-num4);
        long num5 = 10;
        double num6 = 7.655d;
        System.out.println(num5/num6);





    }



}
